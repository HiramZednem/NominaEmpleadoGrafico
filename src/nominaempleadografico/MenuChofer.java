    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String Nombre = txtNombre.getText();
        String Apellido = txtApellido.getText();
        String NSS = txtNSS.getText();
        int SalarioBase = Integer.parseInt(txtSalarioBase.getText());
        Double kmRecorridos =Double.parseDouble( txtRecorrido.getText());
        String TipoGasolina = (String) cbGasolina.getSelectedItem();
        
        lista.add(new EmpleadoChofer(Nombre,Apellido,NSS,SalarioBase,kmRecorridos,TipoGasolina));
        JOptionPane.showMessageDialog(null,"Registro Agregado");
        
    }//GEN-LAST:event_btnAgregarActionPerformed

